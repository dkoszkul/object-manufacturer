package pl.manufacturer.object.example.simple;

public class SimpleCharacterObject {

    private Character characterObject;

    private char characterPrimitive;

    public Character getCharacterObject() {
        return characterObject;
    }

    public void setCharacterObject(Character characterObject) {
        this.characterObject = characterObject;
    }

    public char getCharacterPrimitive() {
        return characterPrimitive;
    }

    public void setCharacterPrimitive(char characterPrimitive) {
        this.characterPrimitive = characterPrimitive;
    }
}
